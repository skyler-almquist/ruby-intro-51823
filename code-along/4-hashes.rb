# Hashes are lists of *key-value pairs*
# => is a hash
me = {"name" => "Skyler", 
"location" => {"city"=> "Chicago", "state" => "Illinois"},
"timeline" => [
    {"status" => "office", "posted" => "1:46pm CST"},
    {"status" => "kitchen", "posted" => "1:47pm CST"}
]
}
# puts me

# Accessing data from the hash
puts me["timeline"]



